using System.Collections;

public static class Kata
{
    public static object[] RemoveEveryOther(object[] arr)
    {
        List<T> list = new();
        int i = 1;
        while (i < list.Count)
        {
            list.RemoveAt(i);
            i++;
        }
    }
}

